package com.burnercode;

public class Customer {

    private Integer id;

    private String name;

    private String email;

    private String age;

    public Customer() {
    }

    public Customer(Integer id, String name, String email, String age) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.age = age;
    }
}
